#!/usr/bin/env node
// Mirrors the current employees from the prod admin API
// (GET /api/current-employees/all) into git, doing two writes from one fetch:
//   1. data/current-employees.json — the full store, written like the API
//      writes it so git holds a durable, version-controlled backup of data
//      that otherwise lives only on the prod data volume. Backup only: nothing
//      seeds this back to prod, so it never reverts admin edits.
//   2. .github/workflows/toggle-employee.yml — regenerates the `employee` input
//      `options:` so the Set Employee Status picker reflects the live roster,
//      including disabled profiles (the /all endpoint returns all three states).
//
// Run by the "Sync Admin Data → Git" workflow (schedule + manual dispatch);
// the workflow commits whichever files changed. Also safe to run manually with
// the env vars set.
//
// Required env:
//   EMPLOYEES_API_URL        e.g. https://findcarehelper.com/api
//   EMPLOYEES_SERVICE_TOKEN  matches the api's EMPLOYEES_SERVICE_TOKEN
//
// Exit codes:
//   0  data + options written (or already in sync)
//   1  could not fetch employees or locate the sentinel markers

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const HERE = dirname(fileURLToPath(import.meta.url));
const WORKFLOW = resolve(HERE, "../.github/workflows/toggle-employee.yml");
const EMPLOYEES_DATA = resolve(HERE, "../data/current-employees.json");

const BEGIN_MARK =
  "# >>> auto-generated by scripts/sync-employee-workflow-options.mjs — do not edit";
const END_MARK = "# <<< end auto-generated";

async function fetchEmployeesFile() {
  const apiUrl = (process.env.EMPLOYEES_API_URL ?? "").replace(/\/$/, "");
  const token = process.env.EMPLOYEES_SERVICE_TOKEN ?? "";
  if (!apiUrl || !token) {
    throw new Error(
      "EMPLOYEES_API_URL and EMPLOYEES_SERVICE_TOKEN must be set in the environment.",
    );
  }
  // /all returns the full roster (active + placed + disabled) and accepts the
  // service token, so the dropdown can target any profile in any state.
  const res = await fetch(`${apiUrl}/current-employees/all`, {
    headers: { "X-Service-Token": token },
  });
  if (!res.ok) {
    throw new Error(
      `GET /current-employees/all responded ${res.status}: ${await res.text()}`,
    );
  }
  const data = await res.json();
  // Return the store shape the API persists ({ employees, updatedAt }) so the
  // file we write to git matches the prod volume copy byte-for-byte.
  return {
    employees: Array.isArray(data.employees) ? data.employees : [],
    updatedAt: typeof data.updatedAt === "string" ? data.updatedAt : "",
  };
}

// Unique, non-empty full names in first-seen order. A GitHub choice option must
// be unique; in the rare case two candidates share a name, collapse them — the
// toggle script resolves the slug by name at run time.
function uniqueNames(employees) {
  const seen = new Set();
  const names = [];
  for (const e of employees) {
    const name = typeof e?.name === "string" ? e.name.trim() : "";
    if (name && !seen.has(name)) {
      seen.add(name);
      names.push(name);
    }
  }
  return names;
}

// Write the full store with the same formatting the API uses (2-space indent +
// trailing newline) so an unchanged store yields no git diff and the committed
// file matches the one on the prod volume.
function writeEmployeesData(file) {
  const json = JSON.stringify(
    { employees: file.employees, updatedAt: file.updatedAt },
    null,
    2,
  );
  writeFileSync(EMPLOYEES_DATA, json + "\n");
}

function rewriteOptions(yaml, names) {
  const beginIdx = yaml.indexOf(BEGIN_MARK);
  const endIdx = yaml.indexOf(END_MARK);
  if (beginIdx < 0 || endIdx < 0 || endIdx < beginIdx) {
    throw new Error(
      "Could not locate sentinel markers in toggle-employee.yml — has someone hand-edited it?",
    );
  }

  // Leading whitespace of the BEGIN line, reused so the regenerated options
  // keep the same YAML indentation as the surrounding list.
  const lineStart = yaml.lastIndexOf("\n", beginIdx) + 1;
  const indent = yaml.substring(lineStart, beginIdx);

  // JSON.stringify yields a double-quoted scalar that is valid YAML and safely
  // escapes any quotes/specials in a name.
  const optionLines = names.map((n) => `${indent}- ${JSON.stringify(n)}`);
  const body = optionLines.length > 0 ? "\n" + optionLines.join("\n") : "";
  const replacement = `${BEGIN_MARK}${body}\n${indent}${END_MARK}`;

  const endLineEnd = yaml.indexOf("\n", endIdx);
  const tail = endLineEnd >= 0 ? yaml.substring(endLineEnd) : "";

  return yaml.substring(0, lineStart) + indent + replacement + tail;
}

async function main() {
  let file;
  try {
    file = await fetchEmployeesFile();
  } catch (err) {
    console.error(`Failed to fetch employees: ${err.message}`);
    process.exit(1);
  }

  // 1) Mirror the full store into git as the durable backup copy.
  try {
    writeEmployeesData(file);
  } catch (e) {
    console.error(`Failed to write ${EMPLOYEES_DATA}: ${e.message}`);
    process.exit(1);
  }
  console.log(
    `Mirrored ${file.employees.length} employees into data/current-employees.json.`,
  );

  // 2) Regenerate the Set Employee Status dropdown from the same data.
  const names = uniqueNames(file.employees);
  if (names.length === 0) {
    console.error("No employees found — refusing to write an empty dropdown.");
    process.exit(1);
  }

  let yaml;
  try {
    yaml = readFileSync(WORKFLOW, "utf8");
  } catch (e) {
    console.error(`Workflow file not found at ${WORKFLOW}: ${e.message}`);
    process.exit(1);
  }

  let next;
  try {
    next = rewriteOptions(yaml, names);
  } catch (e) {
    console.error(e.message);
    process.exit(1);
  }

  if (next === yaml) {
    console.log(`Employee dropdown already in sync (${names.length} employees).`);
    return;
  }
  writeFileSync(WORKFLOW, next);
  console.log(`Wrote ${names.length} employee names into the dropdown.`);
}

main();
