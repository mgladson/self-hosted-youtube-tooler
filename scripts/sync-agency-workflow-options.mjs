#!/usr/bin/env node
// Mirrors the saved receiving agencies from the prod admin API
// (GET /api/agencies) into git, doing two writes from one fetch:
//   1. data/agencies.json — the full store, written byte-for-byte like the API
//      writes it so git holds a durable, version-controlled backup of agencies
//      that otherwise live only on the prod data volume. Backup only: nothing
//      seeds this back to prod, so it never reverts admin edits.
//   2. .github/workflows/create-invoice.yml — regenerates the `options:` list
//      for the `agency` input so the Create Invoice picker reflects the current
//      agencies. The fixed "Type a new agency below" option lives outside the
//      markers and is preserved.
//
// Run by the "Sync Agency Dropdown" workflow on demand (manual dispatch); the
// workflow commits whichever of the two files changed.
//
// Required env:
//   EMPLOYEES_API_URL        e.g. https://findcarehelper.com/api
//   EMPLOYEES_SERVICE_TOKEN  matches the api's EMPLOYEES_SERVICE_TOKEN
//
// Exit codes:
//   0  data + options written (or already in sync)
//   1  could not fetch agencies or locate the sentinel markers

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const HERE = dirname(fileURLToPath(import.meta.url));
const WORKFLOW = resolve(HERE, "../.github/workflows/create-invoice.yml");
const AGENCIES_DATA = resolve(HERE, "../data/agencies.json");

const BEGIN_MARK =
  "# >>> auto-generated by scripts/sync-agency-workflow-options.mjs — do not edit";
const END_MARK = "# <<< end auto-generated";

async function fetchAgenciesFile() {
  const apiUrl = (process.env.EMPLOYEES_API_URL ?? "").replace(/\/$/, "");
  const token = process.env.EMPLOYEES_SERVICE_TOKEN ?? "";
  if (!apiUrl || !token) {
    throw new Error(
      "EMPLOYEES_API_URL and EMPLOYEES_SERVICE_TOKEN must be set in the environment.",
    );
  }
  const res = await fetch(`${apiUrl}/agencies`, {
    headers: { "X-Service-Token": token },
  });
  if (!res.ok) {
    throw new Error(`GET /agencies responded ${res.status}: ${await res.text()}`);
  }
  const data = await res.json();
  // Return the store shape the API persists ({ agencies, updatedAt }) so the
  // file we write to git matches the prod volume copy byte-for-byte.
  return {
    agencies: Array.isArray(data.agencies) ? data.agencies : [],
    updatedAt: typeof data.updatedAt === "string" ? data.updatedAt : "",
  };
}

// Unique, non-empty agency names in first-seen order. A GitHub choice option
// must be unique, but the agencies store allows duplicate names (different
// branches), so collapse them — the invoice script still resolves address/email
// by name at run time.
function uniqueAgencyNames(agencies) {
  const seen = new Set();
  const names = [];
  for (const a of agencies) {
    const name = typeof a?.name === "string" ? a.name.trim() : "";
    if (name && !seen.has(name)) {
      seen.add(name);
      names.push(name);
    }
  }
  return names;
}

// Write the full store to data/agencies.json with the same formatting the API
// uses (2-space indent + trailing newline), so an unchanged store yields no git
// diff and the committed file is identical to the one on the prod volume.
function writeAgenciesData(file) {
  const json = JSON.stringify(
    { agencies: file.agencies, updatedAt: file.updatedAt },
    null,
    2,
  );
  writeFileSync(AGENCIES_DATA, json + "\n");
}

function rewriteOptions(yaml, names) {
  const beginIdx = yaml.indexOf(BEGIN_MARK);
  const endIdx = yaml.indexOf(END_MARK);
  if (beginIdx < 0 || endIdx < 0 || endIdx < beginIdx) {
    throw new Error(
      "Could not locate sentinel markers in create-invoice.yml — has someone hand-edited it?",
    );
  }

  // Leading whitespace of the BEGIN line, reused so the regenerated options
  // keep the same YAML indentation as the surrounding list.
  const lineStart = yaml.lastIndexOf("\n", beginIdx) + 1;
  const indent = yaml.substring(lineStart, beginIdx);

  // JSON.stringify yields a double-quoted scalar that is valid YAML and safely
  // escapes any quotes/specials in an agency name.
  const optionLines = names.map((n) => `${indent}- ${JSON.stringify(n)}`);
  const body = optionLines.length > 0 ? "\n" + optionLines.join("\n") : "";
  const replacement = `${BEGIN_MARK}${body}\n${indent}${END_MARK}`;

  const endLineEnd = yaml.indexOf("\n", endIdx);
  const tail = endLineEnd >= 0 ? yaml.substring(endLineEnd) : "";

  return yaml.substring(0, lineStart) + indent + replacement + tail;
}

async function main() {
  let file;
  try {
    file = await fetchAgenciesFile();
  } catch (err) {
    console.error(`Failed to fetch agencies: ${err.message}`);
    process.exit(1);
  }

  // 1) Mirror the full store into git as the durable backup copy. Always
  // written; git decides whether anything actually changed.
  try {
    writeAgenciesData(file);
  } catch (e) {
    console.error(`Failed to write ${AGENCIES_DATA}: ${e.message}`);
    process.exit(1);
  }
  console.log(`Mirrored ${file.agencies.length} agencies into data/agencies.json.`);

  // 2) Regenerate the Create Invoice dropdown from the same data.
  const names = uniqueAgencyNames(file.agencies);
  let yaml;
  try {
    yaml = readFileSync(WORKFLOW, "utf8");
  } catch (e) {
    console.error(`Workflow file not found at ${WORKFLOW}: ${e.message}`);
    process.exit(1);
  }

  let next;
  try {
    next = rewriteOptions(yaml, names);
  } catch (e) {
    console.error(e.message);
    process.exit(1);
  }

  if (next === yaml) {
    console.log(`Agency dropdown already in sync (${names.length} agencies).`);
    return;
  }
  writeFileSync(WORKFLOW, next);
  console.log(`Wrote ${names.length} agency names into the dropdown.`);
}

main();
